    private void PairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PairActionPerformed
        // TODO add your handling code here:
        defaul();
        try{
            File file = new File("today.txt");
            in = new Scanner(file);
            int i = 0;
            while(in.hasNext()){
                map[i]=(int)(in.next().charAt(0));
                if(map[i] < A || map[i]>Z){
                    defaul();
                    System.out.println("ERROR, pairing not compatable, using default");
                    break;
                }
                for(int k=i-1; i<0; i--){
                    if(map[i] == map[k]){
                        defaul();
                        System.out.println("ERROR, pairing not compatable, using default");
                        break;
                    }
                }
                //System.out.println((char)map[i]);
                i++;
            }
            
            in.close();
        }catch(Exception e){
            System.out.println("ERROR, Pairing Failed, using default\n"+e);
        }
        
    }//GEN-LAST:event_PairActionPerformed

