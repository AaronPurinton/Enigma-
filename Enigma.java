    private void TanslateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TanslateActionPerformed

        String original = originalText.getText().toUpperCase();
        String code = "";
        int r0clone = r0;
        int r1clone = r1;
        int r2clone = r2;
        int length = original.length();
        
        //System.out.println(original);
        
        if(toCo){
        for(int i = 0; i < length; i++,r0clone++){
            
            //added
            if(original.charAt(i) < 65 || original.charAt(i) > 90){ /*r0clone--;*/ code += original.charAt(i); continue;}
            
            if(r0clone>26){
                r0clone = 0;
                r1clone++;
            }
            if(r1clone>26){
                r1clone=0;
                r2clone++;
            }
            if(r2clone>26){
                r2clone=0;
            }
            
            int x = flip(original.charAt(i))-A;//0-25
            //System.out.println("x= "+x);
            code+=((char)(unflip(((x+r0clone+r1clone+r2clone)%26)+A)));
        }
        //r0Label.setText("idaf");
        
        //System.out.println(code);
        translationText.setText(code);
        }
        
        else{
             for(int i = 0; i < length; i++,r0clone++){
                 
                 if(original.charAt(i)<0x41 || original.charAt(i)> 0x5A){/*r0clone--;*/ code += original.charAt(i); continue;}
                 
            if(r0clone>26){
                r0clone = 0;
                r1clone++;
            }
            if(r1clone>26){
                r1clone=0;
                r2clone++;
            }
            if(r2clone>26){
                r2clone=0;
            }
            
            int x = flip(original.charAt(i))-A;//0-25
            //System.out.println("x= "+x);
            //System.out.println(flip(((x-r0clone-r1clone-r2clone)%26)+A));
            //code+=((char)(flip(((x-r0clone-r1clone-r2clone)%26)+A)));
            
            x = (x-r0clone-r1clone-r2clone);
            //System.out.println(x);
            while(x<0){
                x+=26;
            }
            code+=(char)unflip(x+A);
        }
        //r0Label.setText("idaf");
        
        //System.out.println(code);
        translationText.setText(code);
        
        }
    }//GEN-LAST:event_TanslateActionPerformed
    
    private int unflip(int x){
        for(int i = 0; i<26; i++){
            if(x==map[i]){
                return init[i];
            }
        }
        return -1;
    }
    
    private int flip(int x){
        for(int i = 0; i<26; i++){
            if(x==init[i]){
                return map[i];
            }
        }
        return -1;
    }
    
